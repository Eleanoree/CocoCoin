// ============================================================
// 📋 操作結果 — 白話文：任務回報小紙條 / 跑腿結果通知單
// ============================================================
//
// 情境劇：想像你叫一個助理去幫你辦事（例如「去把資料備份到雲端」）
//        助理回來後，會遞給你一張小紙條，上面寫著：
//           ✅ 成功 / ❌ 失敗
//           📝 附註：「已成功備份 128 筆交易」
//
// 這個 OperationResult 就是那張「小紙條」的標準格式！
//
// 為什麼需要它？
//   在寫程式時，很多函式都需要回報「有沒有成功」以及「發生了什麼事」
//   如果每個函式都自己發明一種回報格式（有的回傳 Boolean，有的回傳 String，有的回傳自訂類別）
//   程式會變得超級混亂，難以維護。
//
//   統一使用 OperationResult 的好處：
//     1. 標準化：所有函式都用同一種格式回報
//     2. 好閱讀：一看就知道 success 是成功與否，message 是詳細說明
//     3. 好處理：呼叫方可以用統一的方式處理結果（例如先檢查 success，再顯示 message）
// ============================================================
package com.example.cococoin

// ============================================================
// 📦 OperationResult — 「任務結果包裝盒」
// ============================================================
// data class 的好處：Kotlin 會自動幫你產生 equals()、hashCode()、toString()、copy()
// 白話文：你只要寫出「盒子裡要裝什麼」，Kotlin 會幫你把盒子的其他功能都做好
data class OperationResult(
    val success: Boolean,   // ✅ 操作是否成功？true = 成功，false = 失敗（就像紙條上的「打勾」或「打叉」）
    val message: String? = null  // 📝 附帶的說明文字（例如「備份成功」、「網路連線中斷」、「找不到檔案」...）
    // 注意：message 可以是 null（有時候不需要額外說明，例如「登出成功」就不用廢話）
) {
    // ============================================================
    // 🧰 伴侶物件 — 白話文：工具箱（裡面有兩把方便的「工廠工具」）
    // ============================================================
    companion object {
        // ============================================================
        // ✅ 建立一個「成功」的結果 — 白話文：印一張「任務成功」的小紙條
        // ============================================================
        // 使用方法：
        //   val result = OperationResult.ok("資料已儲存")
        //   // result.success = true, result.message = "資料已儲存"
        //
        // 如果不需要附帶訊息，可以直接寫：
        //   val result = OperationResult.ok()
        //   // result.success = true, result.message = null
        fun ok(message: String? = null) = OperationResult(success = true, message = message)

        // ============================================================
        // ❌ 建立一個「失敗」的結果 — 白話文：印一張「任務失敗」的小紙條，並附上原因
        // ============================================================
        // 使用方法：
        //   val result = OperationResult.fail("網路連線中斷")
        //   // result.success = false, result.message = "網路連線中斷"
        //
        // 注意：失敗時通常「一定」要附上原因，所以 message 不是可選的（沒有預設值）
        // 這樣可以強迫開發者思考「為什麼失敗」，並給使用者一個清楚的說明
        fun fail(message: String) = OperationResult(success = false, message = message)
    }
}