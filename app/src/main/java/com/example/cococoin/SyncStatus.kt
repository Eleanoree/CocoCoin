// ============================================================
// 📊 同步狀態 — 白話文：雲端同步的「健康檢查報告」或「儀表板」
// ============================================================
//
// 情境劇：想像你走進App的「設定」頁面，看到一個區塊寫著：
//
//   ┌─────────────────────────────────────┐
//   │  ☁️ 雲端同步狀態                      │
//   ├─────────────────────────────────────┤
//   │  Firebase 設定：✅ 已就緒            │
//   │  登入狀態：✅ 已登入 (id: abc123)    │
//   │  上次同步：2026/03/26 15:30          │
//   │  同步結果：✅ 成功                   │
//   │  訊息：雲端同步成功，備份了15筆交易   │
//   └─────────────────────────────────────┘
//
// 這個 SyncStatus 類別，就是那張「報告表格」的範本！
// 不管同步成功還是失敗，報告的「格式」都長這個樣子
// 只是裡面的內容會不一樣（成功時是笑臉，失敗時是哭臉）
//
// data class 是什麼？白話文：Kotlin 幫你自動生成「拿資料、比對資料、複製資料」的懶人神器！
// 你只需要寫這一行的欄位定義，它就自動幫你生出 equals()、hashCode()、toString()、copy()
// ============================================================
package com.example.cococoin

data class SyncStatus(

    // ============================================================
    // 🔌 Firebase 是否已就緒？ — 白話文：雲端大門有沒有打開？
    // ============================================================
    //
    // 情境劇：
    //   - true：Firebase 已經設定好，可以開始同步（就像雲端倉庫的鑰匙已經插進鎖孔）
    //   - false：Firebase 還沒設定（通常是 App 第一次安裝，或初始化失敗）
    //
    // 為什麼需要這個欄位？
    //   因為 Firebase 需要一些初始化步驟（網路權限、Google 服務設定...）
    //   如果還沒設定好就嘗試同步，App 會當機！
    //   所以先檢查這個欄位，確定「門打開了」才走進去
    val isFirebaseConfigured: Boolean,

    // ============================================================
    // 🔐 使用者是否已登入？ — 白話文：你是誰？你有門禁卡嗎？
    // ============================================================
    //
    // 情境劇：
    //   - true：使用者已經用 Google 或 Email 登入，我們知道他是誰
    //   - false：使用者是「訪客模式」，沒有登入（資料只存在手機本地，不會上雲端）
    //
    // 為什麼需要這個欄位？
    //   雲端同步需要知道「這筆資料是誰的」
    //   沒登入的話，資料就沒有主人，不能上傳（不然會跟別人的資料混在一起）
    //   就像寄包裹要有寄件人地址一樣！
    val isSignedIn: Boolean,

    // ============================================================
    // 🆔 使用者的唯一編號 — 白話文：你的身分證字號
    // ============================================================
    //
    // 情境劇：
    //   - 有登入：userId = "abc123xyz"（Firebase 給的一串亂碼，但全世界唯一）
    //   - 沒登入：userId = null（沒有身分證字號）
    //
    // 這個欄位是用來：
    //   1. 在雲端資料庫裡區分「這是小明的資料」還是「小華的資料」
    //   2. 登出後再登入，用同一個 userId 可以把之前備份的資料抓回來
    //
    // 注意：這個 userId 不是使用者自己設的帳號名稱，而是 Firebase 自動產生的
    //      就像你去辦護照，政府會給你一個護照號碼，不是你自己的名字
    val userId: String?,

    // ============================================================
    // ⏰ 上次同步的時間 — 白話文：最後一次備份是什麼時候？
    // ============================================================
    //
    // 情境劇：
    //   - 有同步過：lastSyncAt = 1743000000000（換算成人類時間：2026/03/26 15:30）
    //   - 沒同步過：lastSyncAt = null（從來沒備份過）
    //
    // 儲存方式：毫秒（Long）
    //   為什麼不用 String？因為 Long 數字可以直接比大小（誰比較新）
    //   顯示給使用者看的時候再轉換成「2026/03/26 15:30」這種格式就好
    //
    // 用途：
    //   1. 在設定頁面顯示「上次備份：3分鐘前」
    //   2. 判斷是否需要自動同步（例如超過 1 小時沒同步就自動跑一次）
    val lastSyncAt: Long?,

    // ============================================================
    // ✅❌ 上次同步是否成功？ — 白話文：上次備份是笑臉還是哭臉？
    // ============================================================
    //
    // 情境劇：
    //   - true：成功！（顯示綠色勾勾 ✅，使用者安心）
    //   - false：失敗！（顯示紅色驚嘆號 ❌，提醒使用者檢查網路）
    //   - null：從來沒同步過（顯示灰色圓圈 ⚪，代表「尚未發生」）
    //
    // 為什麼可以用 null？
    //   因為「沒發生過」跟「發生過但失敗」是兩種不同的狀態！
    //   就像考試：
    //     - null：還沒考過（沒有成績）
    //     - false：考過了但不及格（有成績但很差）
    //     - true：考過了且及格（成績很好）
    //
    // 這樣 UI 就可以分別顯示：
    //   - null：「從未同步，點擊按鈕開始第一次備份」
    //   - false：「上次同步失敗，請檢查網路連線」
    //   - true：「同步成功，資料已安全備份」
    val lastSyncSuccessful: Boolean?,

    // ============================================================
    // 💬 上次同步的結果說明 — 白話文：詳細的「備註欄」
    // ============================================================
    //
    // 情境劇：
    //   成功時：「雲端同步成功，備份了 15 筆交易」
    //   失敗時：「網路連線中斷，請檢查 Wi-Fi 或行動網路」
    //   登出時：「已登出，請重新登入以找回雲端資料」
    //   從未同步：「尚未進行雲端同步」
    //
    // 這個欄位是 String（字串），永遠不會是 null（有預設值）
    // 用途：顯示給使用者看的「白話文訊息」，比上面那些 true/false 更容易理解
    //
    // 為什麼需要這個欄位？
    //   因為只看 lastSyncSuccessful 的 true/false，使用者只知道「成功/失敗」
    //   但不知道「為什麼失敗」或「成功備份了幾筆」
    //   這個欄位可以寫更多細節，讓使用者知道發生什麼事
    val lastSyncMessage: String
)