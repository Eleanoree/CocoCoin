// ============================================================
// 🏗️ 專案層級 Gradle 設定檔 — 白話文：建築工地的「總體施工說明書」
// ============================================================
//
// 情境劇：想像你要蓋一棟大樓（CocoCoin App）
//         這個檔案就是「總體施工說明書」，它告訴工人們：
//           「我們會用到這些工具，這是工具的型號」
//           但「真正開始用」要等到每個樓層的細部計畫
//
// 為什麼要分兩層？
//   - 最外層（這個檔案）：宣告有哪些工具可用、版本多少
//   - 內層（app/build.gradle.kts）：真正啟用特定工具
//
// 這樣做的好處：所有模組共用同一份「工具版本」，不會 A 模組用版本 8.5.2，
// B 模組用版本 8.6.0 導致衝突
// ============================================================

// ============================================================
// 🔧 施工工具清單 — 白話文：工地要用到的設備型錄
// ============================================================
// plugins 區塊列出整個專案會用到的所有「施工工具」
plugins {
    // ============================================================
    // apply false 是什麼？ — 白話文：先買工具放在倉庫，但先不用
    // ============================================================
    // 「apply false」的意思是：先宣告有這個工具，記錄它的版本
    // 但真正「啟用」要等到子模組（例如 app）用 apply 來呼叫
    //
    // 就像公司先買了一批工具放在倉庫，哪個工地要用再領去現場

    // ============================================================
    // 🤖 Android App 建置工具 — 白話文：把設計圖變成實體房子的機器
    // ============================================================
    id("com.android.application") version "8.5.2" apply false
    // 這是 Android 官方最重要的工具！
    // 它的工作：
    //   1. 把 Kotlin/Java 程式碼編譯成 DEX（手機看得懂的格式）
    //   2. 把資源檔案（圖片、佈局）打包進去
    //   3. 最後產出 APK 或 AAB 檔案（可以安裝到手機的檔案）
    //
    // 版本 8.5.2：Agp（Android Gradle Plugin）的版本
    // 通常要跟 Android Studio 版本搭配

    // ============================================================
    // 📝 Kotlin 語言支援 — 白話文：讓 Android 聽得懂 Kotlin
    // ============================================================
    id("org.jetbrains.kotlin.android") version "2.0.0" apply false
    // Kotlin 是我們寫程式用的語言（不是 Java）
    // 這個工具負責：
    //   1. 把 Kotlin 程式碼編譯成 Java 位元組碼
    //   2. 處理 Kotlin 的特殊語法（如 data class、協程）
    //
    // 版本 2.0.0：Kotlin 語言版本（越新語法越多，但要確認相容性）

    // ============================================================
    // 🎨 Compose UI 工具 — 白話文：用程式碼畫畫面的機器
    // ============================================================
    id("org.jetbrains.kotlin.plugin.compose") version "2.0.0" apply false
    // Jetpack Compose 是 Google 新一代的 UI 框架
    // 傳統寫法：用 XML 設計畫面（像畫圖）
    // Compose 寫法：用 Kotlin 程式碼寫畫面（像寫作文）
    //
    // 這個工具負責讓 Compose 的語法（如 @Composable）正常運作

    // ============================================================
    // 🔮 KSP（Kotlin Symbol Processing）— 白話文：會自動寫程式的魔法工具
    // ============================================================
    id("com.google.devtools.ksp") version "2.0.0-1.0.24" apply false
    // KSP 是一個「程式碼產生器」
    // 你寫一個簡單的註解（如 @Entity），KSP 會幫你自動產生一堆程式碼
    //
    // 誰需要它？Room 資料庫！
    // 你寫了 interface TransactionDao，KSP 會自動生出實作程式碼
    //
    // 版本格式：2.0.0-1.0.24
    //   - 2.0.0：對應的 Kotlin 版本
    //   - 1.0.24：KSP 自己的版本

    // ============================================================
    // ☁️ Google 服務工具 — 白話文：串接 Google 雲端服務的接頭
    // ============================================================
    id("com.google.gms.google-services") version "4.4.4" apply false
    // 這個工具負責處理 Firebase 相關設定
    // 它會讀取 google-services.json 這個設定檔
    // 讓 Firebase Auth、Firestore 等功能可以正常運作
    //
    // 如果你不用 Firebase，這個可以不用加
    // 但我們需要雲端備份和登入功能，所以必須加！
}

