// ============================================================
// 🔐 帳號連結狀態 — 白話文：帳號的「履歷表」或「登入狀態體檢報告」
// ============================================================
// 
// 情境劇：想像你走進 App 的「設定」頁面，畫面會根據你目前的登入狀態
//         顯示不同的按鈕組合：
//
//   🔧 情況 1：沒登入 → 顯示「Google 登入」和「信箱登入」按鈕
//   🥸 情況 2：匿名登入 → 顯示「升級會員」按鈕（綁定 Google 或信箱）
//   ⭐ 情況 3：完整會員 → 顯示「備份」、「新增登入方式」、「登出」等完整功能
//
// 這個 AuthLinkStatus 就是那張「你是誰？你和雲端的關係怎麼樣？」的報告！
// 設定頁面讀取這張報告，就知道要給使用者看哪些按鈕
// ============================================================
package com.example.cococoin

data class AuthLinkStatus(
    // ============================================================
    // 🔌 Firebase 是否已設定好？
    // ============================================================
    // 白話文：雲端大門有沒有打開？
    //   - true：Firebase 已經設定好，可以開始同步（google-services.json 正確）
    //   - false：Firebase 還沒設定（通常是測試環境或初始化失敗）
    val isFirebaseReady: Boolean,

    // ============================================================
    // 🔐 使用者是否已登入？
    // ============================================================
    // 白話文：你現在有沒有拿到「門禁卡」？
    //   - true：已經登入了（可能是匿名、Google、或 Email）
    //   - false：還沒登入（路人甲）
    val isSignedIn: Boolean,

    // ============================================================
    // 🥸 是否是匿名登入？
    // ============================================================
    // 白話文：你現在是「訪客模式」還是「正式會員」？
    //   - true：訪客模式（只在這支手機有資料，換手機就消失）
    //   - false：正式會員（有綁定 Google 或信箱，資料跟著帳號走）
    //
    // 注意：如果 isSignedIn = false，那麼 isAnonymous 一定是 false
    //      因為沒登入的人不可能是「匿名登入」
    val isAnonymous: Boolean,

    // ============================================================
    // 🆔 使用者的唯一識別碼
    // ============================================================
    // 白話文：你的「雲端身分證字號」
    //   - 有登入：uid = "abc123xyz"（Firebase 自動產生的一串亂碼，全世界唯一）
    //   - 沒登入：uid = null
    //
    // 這個 UID 是用來在雲端資料庫區分「這是小明的資料」還是「小華的資料」
    val uid: String?,

    // ============================================================
    // 📧 使用者的 Email
    // ============================================================
    // 白話文：你有綁定 Email 嗎？有的話是哪個信箱？
    //   - 有綁 Email：email = "user@example.com"
    //   - 沒綁 Email：email = null（可能是匿名登入或只用 Google 登入）
    val email: String?,

    // ============================================================
    // 📧✉️ 是否已綁定 Email 登入方式？
    // ============================================================
    // 白話文：你有沒有申請「信箱 + 密碼」這種登入方式？
    //   - true：可以用 Email + 密碼登入
    //   - false：還沒綁定 Email（可能只用 Google 登入或是匿名）
    val hasEmailProvider: Boolean,

    // ============================================================
    // 🔴✉️ 是否已綁定 Google 登入方式？
    // ============================================================
    // 白話文：你有沒有申請「用 Google 帳號登入」這種方式？
    //   - true：可以用 Google 帳號登入
    //   - false：還沒綁定 Google（可能只用 Email 登入或是匿名）
    val hasGoogleProvider: Boolean,

    // ============================================================
    // ✅📧 Email 是否已完成驗證？
    // ============================================================
    // 白話文：你綁定的信箱有沒有去收驗證信並點擊確認？
    //   - true：已經驗證過了（安全等級較高）
    //   - false：還沒驗證（可能只是輸入了信箱但沒去點確認信）
    //
    // 為什麼需要驗證？
    //   確保這個 Email 是真的屬於你，不是亂填的！
    //   也防止有人用別人的信箱註冊
    val isEmailVerified: Boolean
)