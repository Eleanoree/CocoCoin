// ============================================================
// 📊 交易資料表 — 白話文：記帳本的「帳本內頁」
// ============================================================
//
// 情境劇：想像你有一本實體的記帳本，裡面每一頁寫著：
//   - 第 1 筆：2026/03/26 買了雞排 80 元，從現金帳戶扣款
//   - 第 2 筆：2026/03/27 收到薪水 50000 元，存入銀行帳戶
//   - 第 3 筆：2026/03/28 搭捷運 30 元，用悠遊卡
//
// 這個 Transaction 類別，就是那「每一頁」的長相範本！
// 不管你記 100 筆還是 10000 筆，每一筆都長這個樣子（有 id、金額、時間...）
//
// @Entity 是什麼？白話文：跟 Room 資料庫說「喂，這個類別要變成一張資料表喔！」
// tableName = "transactions"  → 這張表的名字就叫 transactions（可以自己取名）
// ============================================================
package com.example.cococoin

import androidx.room.ColumnInfo   // 告訴 Room「這個欄位對應到資料庫的哪一列」（就像貼標籤）
import androidx.room.Entity       // 標記這個類別是一張「資料表」（就像蓋章認證）
import androidx.room.PrimaryKey   // 標記哪個欄位是「主鍵」（就像身分證字號，不能重複）

@Entity(tableName = "transactions")  // 「從今天起，你就是一張名叫 transactions 的資料表了！」
data class Transaction(

    // ============================================================
    // 🔑 主鍵（Primary Key）— 白話文：每筆交易的「身分證字號」
    // ============================================================
    //
    // 為什麼需要 id？
    //   因為即使兩筆交易「金額、時間、備註」都一模一樣（例如同一天在同一家店買兩次雞排）
    //   資料庫還是需要一個「絕對不會重複」的編號來區分它們
    //
    // autoGenerate = true → 白話文：交給資料庫自動產生號碼，你不需要自己填
    //   就像銀行的交易序號，你按下確定它就自動跳一個新號碼出來
    //   從 1, 2, 3, 4... 一直往上加，保證不重複
    //
    // @ColumnInfo(name = "id") → 白話文：在資料庫裡，這個欄位的名字就叫「id」
    //   雖然 Kotlin 變數名稱也叫 id，但寫出來比較清楚（強迫症專用）
    @PrimaryKey(autoGenerate = true)
    @ColumnInfo(name = "id")
    val id: Int,

    // ============================================================
    // 📌 類型（type）— 白話文：這是「花錢」還是「賺錢」？
    // ============================================================
    //
    // 內容只會有兩種：「支出」或「收入」
    //   - 「支出」：錢從你的帳戶飛走了（嗚嗚）
    //   - 「收入」：錢飛進你的帳戶了（耶耶）
    //
    // 為什麼需要這個欄位？
    //   因為記帳的時候，支出用紅色，收入用綠色（心裡的感受不同😂）
    //   而且統計圖表也要分開計算
    @ColumnInfo(name = "type")
    val type: String,

    // ============================================================
    // 🏷️ 分類（category）— 白話文：這筆錢「花在哪裡」或「從哪裡來」？
    // ============================================================
    //
    // 支出範例：「餐飲」、「交通」、「購物」、「娛樂」
    // 收入範例：「薪水」、「獎金」、「紅包」、「投資收益」
    //
    // 使用者可以自己新增/修改/刪除分類（這就是為什麼前面有 TransactionCategoryStore）
    // 這個欄位存的只是「分類的名稱」，例如「餐飲」
    @ColumnInfo(name = "category")
    val category: String,

    // ============================================================
    // 💰 金額（amount）— 白話文：多少錢？數字越大心越痛（或越爽）
    // ============================================================
    //
    // 為什麼用 Int 而不是 Double？
    //   - 台灣最小單位是 1 元，用 Int 就好（不需要小數點）
    //   - Int 運算比較快，也不會遇到浮點數誤差（0.1 + 0.2 不等於 0.3 那種鬼故事）
    //   - 如果真的需要角或分，之後再改成以「分」為單位儲存（例如 80 元 = 8000 分）
    @ColumnInfo(name = "amount")
    val amount: Int,

    // ============================================================
    // 📝 備註（note）— 白話文：使用者想額外寫的「小紙條」
    // ============================================================
    //
    // 範例情境：
    //   - 「跟小美一起吃火鍋，她付了湯底錢」
    //   - 「這個月的 Netflix 訂閱費用」
    //   - 「尾牙抽到紅包，爽！」
    //
    // 這個欄位可以是空白（使用者沒寫就是不寫）
    // 通常顯示在交易清單的小字部分
    @ColumnInfo(name = "note")
    val note: String,

    // ============================================================
    // 🕐 交易時間（time）— 白話文：這筆錢「什麼時候花的/賺的」？
    // ============================================================
    //
    // 注意！這個欄位是 String（字串），不是 Long（毫秒）
    // 為什麼？因為使用者看到的是「2026/03/26 15:30」這種格式
    // 存成字串直接拿來顯示比較方便
    //
    // 如果需要排序，可以再用一個 Long 欄位存時間戳（用 updatedAt 代替）
    // 或是用 SQLite 的日期函式來處理
    //
    // 這個格式通常是：yyyy/MM/dd HH:mm（例如 2026/03/26 15:30）
    @ColumnInfo(name = "time")
    val time: String,

    // ============================================================
    // 🏦 帳戶名稱（account_name）— 白話文：這筆錢「從哪個口袋進出」？
    // ============================================================
    //
    // 情境劇：
    //   - 買早餐用「現金」付（錢包直接掏出鈔票）
    //   - 刷信用卡用「國泰銀行」帳戶（之後要繳卡費）
    //   - 收到薪水匯入「郵局」帳戶
    //
    // 這個欄位對應到「帳戶管理」裡面使用者建立的帳戶名稱
    // 例如：「現金」、「國泰信用卡」、「永豐大戶」、「Line Pay Money」
    @ColumnInfo(name = "account_name")
    val accountName: String,

    // ============================================================
    // ⏰ 更新時間（updated_at）— 白話文：這筆資料「最後一次被修改的時間」
    // ============================================================
    //
    // 這個欄位很重要！用途：
    //   1. 同步功能：兩台裝置之間比對「誰的資料比較新」
    //   2. 排序：可以依照「最後修改時間」排序，顯示最近動過的交易
    //   3. 衝突處理：兩個人同時編輯同一筆時，以時間晚的為主
    //
    // 儲存方式：毫秒（Long）
    //   例如 System.currentTimeMillis() 會回傳一個很大的數字：1743000000000
    //   這個數字是可以換算回人類閱讀的時間
    //
    // 預設值 = System.currentTimeMillis()
    //   白話文：如果沒有特別給 updatedAt，就自動填入「現在這一秒」
    //   這樣新建交易的時候就不用自己手動填時間了，方便！
    @ColumnInfo(name = "updated_at")
    val updatedAt: Long = System.currentTimeMillis()
)