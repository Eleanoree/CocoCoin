// ============================================================
// 💰 資產帳戶資料類別 — 白話文：每個錢包的「身分證」或「履歷表」
// ============================================================
//
// 情境劇：想像你的錢分散在不同的地方：
//           👛 皮夾裡的現金（餘額 5,000 元）
//           💳 信用卡（餘額 -2,000 元，表示欠銀行 2,000）
//           🏦 銀行戶頭（餘額 50,000 元）
//           📱 Line Pay（餘額 1,200 元）
//           🎮 遊戲錢包（餘額 300 元）
//
// 這個 AssetAccount 就是記錄每個錢包的「身分證」！
// 它會存在 Room 資料庫的 accounts 表格中
//
// @Entity — 告訴 Room：「這個類別要變成一張資料表喔！」
// ============================================================
package com.example.cococoin

import androidx.room.ColumnInfo
import androidx.room.Entity
import androidx.room.Index
import androidx.room.PrimaryKey

@Entity(
    tableName = "accounts",  // 📋 表格名稱叫 accounts（帳戶清單）
    indices = [Index(value = ["name"], unique = true)]  // 🔑 索引：帳戶名稱必須唯一
    // 白話文：不能有兩個同名的帳戶！例如不能有兩個「現金」帳戶
)
data class AssetAccount(
    // ============================================================
    // 🔢 帳戶編號（主鍵） — 白話文：錢包的「身分證字號」
    // ============================================================
    // autoGenerate = true：讓資料庫自動產生編號（1, 2, 3...）
    // 你不需要自己填 id，Room 會幫你處理
    @PrimaryKey(autoGenerate = true)
    @ColumnInfo(name = "id")
    val id: Int,
    // 白話文：「你的身分證字號是 A123456789」

    // ============================================================
    // 🏷️ 帳戶名稱 — 白話文：錢包的「名字」
    // ============================================================
    // 常見的帳戶名稱：
    //   - 「現金」（皮夾裡的鈔票和零錢）
    //   - 「國泰信用卡」（那張藍色的信用卡）
    //   - 「郵局」（郵局存款帳戶）
    //   - 「Line Pay」（手機支付）
    @ColumnInfo(name = "name")
    val name: String,
    // 白話文：「你的名字是『現金』」

    // ============================================================
    // 💰 帳戶餘額 — 白話文：錢包裡有多少錢？
    // ============================================================
    // 注意：餘額可以是正數（有錢）也可以是負數（負債）
    //   - 正數：現金帳戶、銀行存款
    //   - 負數：信用卡（你欠銀行錢）
    //   - 零：空空如也，剛花光
    @ColumnInfo(name = "balance")
    val balance: Int,
    // 白話文：「你目前有 5,000 元」

    // ============================================================
    // ⏰ 更新時間 — 白話文：這張身分證是什麼時候辦的或更新的？
    // ============================================================
    // 用毫秒儲存（Long 型態），例如 System.currentTimeMillis()
    // 用途：
    //   1. 雲端同步時判斷「哪一筆資料比較新」
    //   2. 讓使用者知道帳戶餘額最後是什麼時候更新的
    @ColumnInfo(name = "updated_at")
    val updatedAt: Long = System.currentTimeMillis()
    // 白話文：「這筆資料最後更新於 2026/04/06 14:30」
)