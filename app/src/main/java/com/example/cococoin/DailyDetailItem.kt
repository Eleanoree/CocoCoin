// ============================================================
// 📋 每日明細的資料結構 — 白話文：分析頁面的「交易小卡片」範本
// ============================================================
//
// 情境劇：想像你在 App 的分析頁面，點擊了「3月27日」
//         畫面會跳出一份清單，裡面每一行長得像這樣：
//
//   ┌─────────────────────────────────────┐
//   │ 🍽 餐飲              支出 ・ 現金    │  ← 這是 title + subTitle
//   │                    -NT$ 100        │  ← 這是 amount
//   └─────────────────────────────────────┘
//
// 這個 DailyDetailItem 就是上面「每一行」的設計圖！
// data class 讓 Kotlin 自動幫我們生成 equals()、hashCode()、toString() 等好用功能
// ============================================================
package com.example.cococoin

data class DailyDetailItem(
    val title: String,      // 📌 標題（例如「餐飲」、「交通」、「薪水」）
    val subTitle: String,   // 📝 副標題（例如「支出 ・ 現金」或「收入 ・ 銀行帳戶」）
    val amount: Int,        // 💰 金額（例如 100、5000）
    val type: String        // 🏷️ 類型（「收入」或「支出」— 用來決定文字顏色和符號）
)